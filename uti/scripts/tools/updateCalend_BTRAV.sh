
copy_TCALEND_TI17REQJOBPLAN()
{
	ENV=$1

	bcp  BREF..TCALEND out $DFILT/TCALEND.dat -Udom_gen_ro -PscorRO -S${ENV}_TPO2  -c -b10000 -t';'   -eerr -Jiso_1
	bcp  BEST..TI17REQJOBPLAN out $DFILT/TI17REQJOBPLAN.dat -Udom_gen_ro -PscorRO -S${ENV}_TPO2  -c -b10000 -t';'   -eerr -Jiso_1


	isql -Ubatch -Pomega2-- -SDEV_TPO2 <<EOF

	USE BTRAV
go
	IF OBJECT_ID('TCALEND_${ENV}') IS NOT NULL
	BEGIN
			DROP TABLE TCALEND_${ENV}
	END
go


	select * into BTRAV..TCALEND_${ENV} from  BREF..TCALEND where 1=2

go
	GRANT REFERENCES ON TCALEND_${ENV} TO GDBBATCH
	GRANT SELECT ON TCALEND_${ENV} TO GCONSULT
	GRANT SELECT ON TCALEND_${ENV} TO GDBBATCH
	GRANT INSERT ON TCALEND_${ENV} TO GDBBATCH
	GRANT DELETE ON TCALEND_${ENV} TO GDBBATCH
	GRANT UPDATE ON TCALEND_${ENV} TO GDBBATCH
	GRANT DELETE STATISTICS ON TCALEND_${ENV} TO GDBBATCH
	GRANT TRUNCATE TABLE ON TCALEND_${ENV} TO GDBBATCH
	GRANT UPDATE STATISTICS ON TCALEND_${ENV} TO GDBBATCH
	GRANT TRANSFER TABLE ON TCALEND_${ENV} TO GDBBATCH
go


	IF OBJECT_ID('TI17REQJOBPLAN_${ENV}') IS NOT NULL
	BEGIN
			DROP TABLE TI17REQJOBPLAN_${ENV}
	END
go


	select * into BTRAV..TI17REQJOBPLAN_${ENV} from  BEST..TI17REQJOBPLAN where 1=2

go
	GRANT REFERENCES ON TI17REQJOBPLAN_${ENV} TO GDBBATCH
	GRANT SELECT ON TI17REQJOBPLAN_${ENV} TO GCONSULT
	GRANT SELECT ON TI17REQJOBPLAN_${ENV} TO GDBBATCH
	GRANT INSERT ON TI17REQJOBPLAN_${ENV} TO GDBBATCH
	GRANT DELETE ON TI17REQJOBPLAN_${ENV} TO GDBBATCH
	GRANT UPDATE ON TI17REQJOBPLAN_${ENV} TO GDBBATCH
	GRANT DELETE STATISTICS ON TI17REQJOBPLAN_${ENV} TO GDBBATCH
	GRANT TRUNCATE TABLE ON TI17REQJOBPLAN_${ENV} TO GDBBATCH
	GRANT UPDATE STATISTICS ON TI17REQJOBPLAN_${ENV} TO GDBBATCH
	GRANT TRANSFER TABLE ON TI17REQJOBPLAN_${ENV} TO GDBBATCH



EOF

	bcp  BTRAV..TCALEND_${ENV} in $DFILT/TCALEND.dat -Ubatch -Pomega2-- -SDEV_TPO2  -c -b10000 -t';'   -eerr -Jiso_1
	bcp  BTRAV..TI17REQJOBPLAN_${ENV} in $DFILT/TI17REQJOBPLAN.dat -Ubatch -Pomega2-- -SDEV_TPO2  -c -b10000 -t';'   -eerr -Jiso_1


	isql -Ubatch -Pomega2-- -SDEV_TPO2 <<EOF

	select count(*) from BTRAV..TCALEND_${ENV}
	select count(*) from BTRAV..TI17REQJOBPLAN_${ENV}

go
EOF
}

copy_TCALEND_TI17REQJOBPLAN DEV
copy_TCALEND_TI17REQJOBPLAN PRD
copy_TCALEND_TI17REQJOBPLAN INT
copy_TCALEND_TI17REQJOBPLAN UAT
copy_TCALEND_TI17REQJOBPLAN ITK
