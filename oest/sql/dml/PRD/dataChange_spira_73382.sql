-- Tempory table identifying the transaction codes concerns by the issue (ACY <> UWY)
-- 1 - SCOP ACCEPTE
INSERT INTO #SCOPE_TYP1_A (CTR_NF, SEC_NF, BALSHEY_NF, UWY_NF, ACY_NF, DETTRNCOD_CF, ACMTRS_NT, GAAP_NT, MAX_CRE_D)
SELECT DISTINCT 
LIFEST.CTR_NF, LIFEST.SEC_NF, LIFEST.BALSHEY_NF, LIFEST.UWY_NF, LIFEST.ACY_NF, LIFEST.DETTRNCOD_CF, LIFEST.ACMTRS_NT, LIFEST.GAAP_NT, MAX(LIFEST.CRE_D)
FROM BEST..TLIFEST LIFEST, BTRT..TSECTION TSEC
WHERE LIFEST.CTR_NF = TSEC.CTR_NF
  and LIFEST.SEC_NF = TSEC.SEC_NF
  and LIFEST.UWY_NF = TSEC.UWY_NF
  and TSEC.ACCADMTYP_CT = 1  -- type 1 
  and TSEC.SECSTS_CT   != 19 -- not canceled
  and LIFEST.UWY_NF != LIFEST.ACY_NF
  and LIFEST.ACMTRS_NT not in (1303,1323,1304,1324)
  group by LIFEST.CTR_NF, LIFEST.SEC_NF, LIFEST.BALSHEY_NF, LIFEST.UWY_NF, LIFEST.ACY_NF, LIFEST.DETTRNCOD_CF, LIFEST.ACMTRS_NT, LIFEST.GAAP_NT
  order by LIFEST.CTR_NF, LIFEST.SEC_NF, LIFEST.BALSHEY_NF, LIFEST.UWY_NF, LIFEST.ACY_NF, LIFEST.DETTRNCOD_CF, LIFEST.ACMTRS_NT, LIFEST.GAAP_NT

SELECT  @erreur = @@error
IF @erreur != 0
BEGIN
  PRINT 'SELECT SCOP ACCEPTE - ERROR : %1!', @erreur
  GOTO fin
END


-- 2 - SCOP RETRO
INSERT INTO #SCOPE_TYP1_R (CTR_NF, SEC_NF, BALSHEY_NF, UWY_NF, ACY_NF, DETTRNCOD_CF, ACMTRS_NT, GAAP_NT, MAX_CRE_D)
SELECT DISTINCT 
LIFEST.CTR_NF, LIFEST.SEC_NF, LIFEST.BALSHEY_NF, LIFEST.UWY_NF, LIFEST.ACY_NF, LIFEST.DETTRNCOD_CF, LIFEST.ACMTRS_NT, LIFEST.GAAP_NT, MAX(LIFEST.CRE_D)
FROM BEST..TLIFEST LIFEST, BRET..TRETCTR TRETCTR
WHERE LIFEST.CTR_NF = TRETCTR.RETCTR_NF
  and LIFEST.UWY_NF = TRETCTR.RTY_NF
  and TRETCTR.RETACCTYP_CT  = 1  -- type 1 
  and TRETCTR.RETCTRSTS_CT != 19 -- not canceled
  and LIFEST.UWY_NF != LIFEST.ACY_NF
  and LIFEST.ACMTRS_NT not in (2303,2323,2304,2324)
  group by LIFEST.CTR_NF, LIFEST.SEC_NF, LIFEST.BALSHEY_NF, LIFEST.UWY_NF, LIFEST.ACY_NF, LIFEST.DETTRNCOD_CF, LIFEST.ACMTRS_NT, LIFEST.GAAP_NT
  order by LIFEST.CTR_NF, LIFEST.SEC_NF, LIFEST.BALSHEY_NF, LIFEST.UWY_NF, LIFEST.ACY_NF, LIFEST.DETTRNCOD_CF, LIFEST.ACMTRS_NT, LIFEST.GAAP_NT

