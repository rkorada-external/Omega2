 SSD_CF, CUR_CF, 0.000, INDSUP_B, 'Spira 73382', CREUSR_CF, getdate(), LSTUPDUSR_CF, ORICTR_NF, 
 ORISEC_NF, ORIUWY_NF, 0.000, PROPAGATION_B,
 CALCULATED_B, BATCH_B
FROM
    BEST..TLIFEST LIFEST,
    #SCOPE_TYP1_A SCOPE_TYP1_A
WHERE
        LIFEST.CTR_NF       = SCOPE_TYP1_A.CTR_NF
AND     LIFEST.SEC_NF       = SCOPE_TYP1_A.SEC_NF
AND     LIFEST.BALSHEY_NF   = SCOPE_TYP1_A.BALSHEY_NF
AND     LIFEST.UWY_NF       = SCOPE_TYP1_A.UWY_NF
AND     LIFEST.ACY_NF       = SCOPE_TYP1_A.ACY_NF
