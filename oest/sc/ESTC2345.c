/*----------------------*/
/* variables de travail */
/*----------------------*/
/* Fichiers liés à l'assumed */
FILE        *Kp_NewAccsupIFil;                		 	 // Pointeur sur le fichier NewACCSUP en entree(Pere)
FILE        *Kp_AccsupOutFil;                		 	   // Pointeur sur le fichier ACCSUP en sortie


T_RUPTURE_VAR   Kbd_RuptNewACCSUP;


int BeginCount = 1;
int NumLineNt_Max = 0;


/*==============================================================================
objet :
   point d'entre du programme

retour :
   En cas de probleme, la sortie s'effectue par la fonction ExitPgm().
   Sinon, par l'appel systeme exit()
==============================================================================*/
int main(int argc , char *argv[])
{

  /* Initialisation des signaux */
  InitSig () ;

  if ( n_BeginPgm (argc  , argv) == ERR )
    ExitPgm ( ERR_XX , "" );

  /* Recuperation du numline_nt max*/
  NumLineNt_Max = n_GetIntArgv(1);

  
  if ( n_OpenFileAppl ("ESTC2345_O1", "wt", &Kp_AccsupOutFil) == ERR )
    ExitPgm ( ERR_XX , "" );

  
  /* Initialisation de la variable Kbd_RuptNewACCSUP  */
  if ( n_InitNewACCSUP(&Kbd_RuptNewACCSUP) == ERR )
    ExitPgm ( ERR_XX , "" );

   
  /* lancement du traitement du fichier maitre Assumed */
  if ( n_ProcessingRuptureVar(&Kbd_RuptNewACCSUP) == ERR )
    ExitPgm ( ERR_XX , "" );

  if ( n_CloseFileAppl ("ESTC2345_O1", &Kp_AccsupOutFil) == ERR )
    ExitPgm ( ERR_XX , "" );

    
  if ( n_CloseFileAppl ("ESTC2345_I1", &(Kbd_RuptNewACCSUP.pf_InputFil)) == ERR )
    ExitPgm ( ERR_XX , "" );

   
  if ( n_EndPgm () == ERR )
    ExitPgm ( ERR_XX , "" );

  exit(OK);

}

/*==============================================================================
objet :
    fonction d'initialisation de la variable de gestion de rupture du fichier
    maitre ASSUMED

retour :
  0K ---> traitement correctement effectue
        ERR ---> probleme a l'ouverture du fichier d'entree
==============================================================================*/
static int n_InitNewACCSUP(T_RUPTURE_VAR  *pbd_RuptNewACCSUP)
{
  memset(pbd_RuptNewACCSUP, 0, sizeof(T_RUPTURE_VAR));

  if ( n_OpenFileAppl ("ESTC2345_I1", "rt", &(pbd_RuptNewACCSUP->pf_InputFil)) == ERR)
    return ERR;

  pbd_RuptNewACCSUP->n_NbRupture = 0;
  pbd_RuptNewACCSUP->n_ActionLigne     = n_ActionLigneNewACCSUP;

  pbd_RuptNewACCSUP->c_Separ = '~';

  return OK ;
}


/*==============================================================================
objet :
    Fonction de traitement de chaque enregistrement pere

retour :
  OK --->
  ERR --->
==============================================================================*/

static int  n_ActionLigneNewACCSUP(char *tpsz_ReadBufferNewACCSUP[])
{
	char Kz_NumberOfLine[6];
	/* SPIRA 109928 */
	
	sprintf(Kz_NumberOfLine,"%d", BeginCount);
	tpsz_ReadBufferNewACCSUP[ES_NUMLIGNE_CT]=Kz_NumberOfLine;
	n_WriteCols(Kp_AccsupOutFil, tpsz_ReadBufferNewACCSUP, SEPARATEUR, 0);
	BeginCount++;
  return OK;
}
